package livemd

import "fmt"

type LiveMDStatus struct {
	LiveMD *LiveMD
}

func (x *LiveMD) Status() *LiveMDStatus {
	return &LiveMDStatus{
		LiveMD: x,
	}

}

func (s *LiveMDStatus) String() string {
	var str string
	str += fmt.Sprintf("Title   : %s\n", s.LiveMD.Title)
	str += fmt.Sprintf("Episode : %d\n", s.LiveMD.I)
	return str
}
